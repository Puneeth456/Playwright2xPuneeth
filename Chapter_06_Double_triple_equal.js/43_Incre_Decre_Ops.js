//pre increment
let a = 10;
let b = ++a;  //(increment by the a value by 1);


//Pre-increment  -->++a increase the value by 1 and then assign
console.log(b)
console.log(a)


//De-increment
let a = 10;
let b = --a;
console.log(a);
console.log(b);

//post -decrement

let a = 10;
let b = a--;
console.log(b);
console.log(a);














